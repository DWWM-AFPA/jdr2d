public class Porte {
    private Porte portelie;

    private Point pos;

    //getters


    public Porte getPortelie() {
        return portelie;
    }

    public Point getPos() {
        return pos;
    }





    //setters


    public Porte setPos(Point pos) {
        this.pos = pos;
        return this;
    }

    public Porte setPortelie(Porte portelie) {
        this.portelie = portelie;
        return this;
    }

}
